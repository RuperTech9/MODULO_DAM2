
public class EquipoF1 {

	private String nombre;
	private int[] coches;

	public EquipoF1() {
	}

	public EquipoF1(String nombre, int[] coches) {
		this.nombre = nombre;
		this.coches = coches;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int[] getcoches() {
		return coches;
	}

	public void setcoches(int[] coches) {
		this.coches = coches;
	}

}
